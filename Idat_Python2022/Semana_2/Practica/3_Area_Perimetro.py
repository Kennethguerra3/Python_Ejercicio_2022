#3.	Desarrolle el programa que determine el área y 
# el perímetro de un rectángulo sabiendo que el   área = b x h, perímetro = 2x (b+h).
B=float(input("Ingresa el Valor B: "))
H=float(input("Ingresa el Valor H: "))
Area=B*H
Perimetro=2*(B+H)
print("El Area es: ",Area)
print("EL Perimetro es: ",Perimetro)
