#Leer el sueldo de tres empleados y aplicarles un aumento del 10, 12 y 15% respectivamente. 
Sueldo1=int(input("Ingresa el Sueldo N°1:"))
Sueldo2=int(input("Ingresa el Sueldo N°2:"))
Sueldo3=int(input("Ingresa el Sueldo N°3:"))
s1=Sueldo1*0.10
s2=Sueldo2*0.12
s3=Sueldo3*0.15
print("Sueldo N°1 aumento 10% :",s1,"total Sueldo es=",Sueldo1+s1)
print("Sueldo N°1 aumento 12%:",s2,"total Sueldo es=",Sueldo2+s2)
print("Sueldo N°1 aumento 15%:",s3,"total Sueldo es=",Sueldo3+s3)
