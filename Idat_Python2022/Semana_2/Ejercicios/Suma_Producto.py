#Realizar el pseudocódigo que permita al usuario introducir por teclado dos notas, calculando la suma y el producto de las notas. 
Nota1=float(input("Ingresa la Nota N°1:" ))
Nota2=float(input("Ingresa la Nota N°2:"))
Suma=Nota1+Nota2
producto= Nota1*Nota2
print("La suma de las dos notas es:",Suma)
print("El producto de las dos notas es", producto)
