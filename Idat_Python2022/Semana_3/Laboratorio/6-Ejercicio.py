#6.	Se lee un número correspondiente al radio de la circunferencia, 
# visualizando la longitud de la misma y el área del círculo correspondiente. 
# Se recuerda: AREA = PI * RADIO2 y LONGITUD = 2 * PI * RADIO
radio=float(input("Radio N°1: "))
radio2=float(input("Radio N°2: "))
area=3.14*radio2
longitud=2*3.14*radio
print("Area es = ",area)
print("Longitud es =",longitud)
